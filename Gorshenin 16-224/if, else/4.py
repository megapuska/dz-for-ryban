rim = int(input("Введите число от 1 до 10: "))
if rim == 1:
    print("I")
elif rim == 2:
    print("II")
elif rim == 3:
    print("III")
elif rim == 4:
    print("IV")
elif rim == 5:
    print("V")
elif rim == 6:
    print("VI")
elif rim == 7:
    print("VII")
elif rim == 8:
    print("VIII")
elif rim == 9:
    print("IX")
elif rim == 10:
    print("X")
else:
    print("Ошибка: Число превышает диапазон от 1 до 10")